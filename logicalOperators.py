# Example of logical operators in Python

# Definition of two variables
x = True
y = False

# Logical AND operator
result_and = x and y
print(f"Logical AND: {x} and {y} = {result_and}")

# Logical OR operator
result_or = x or y
print(f"Logical OR: {x} or {y} = {result_or}")

# Logical NOT operator
result_not_x = not x
result_not_y = not y
print(f"Logical NOT of {x}: {result_not_x}")
print(f"Logical NOT of {y}: {result_not_y}")
